<h2>Artists (personal websites)</h2>

<p>This is largely curated for artists with little to no experience in owning a website.</p>

<p>Glossary for beginners:
<ul>
    <li><b>HTML</b> is used for the content of the page</li>
    <li><b>CSS</b> is used for the style of the page (colors, fonts, positions, general layout)</li>
    <li><b>Javascript/scripts</b> are used for how certain elements of a webpage interact</li>
    <li><b>Domains</b> are URLs - the most basic thing people know about your website even if they haven't visited it; think of it like anyone's legal name. Registering a domain (like on <a href="https://porkbun.com/" target="blank">Porkbun</a> or <a href="https://namecheap.com" target="blank">Namecheap</a> &mdash; <font color="red">do not use GoDaddy</font>) is like creating a birth certificate for your website, but does not inherently allow it to go live.</li>
    <li><b>Web hosts</b> (in the context of this glossary and page) and their respective servers are what allow your website to live and exist in the first place. Even if you may have a domain, you will always need a host in order for the domain to direct to a proper website visible on the internet. Examples of hosts include <a href="https://neocities.org" target="blank">Neocities</a> (where anyone can register a subdomain and automatically get hosted by Neocities for free) or <a href="https://leprd.space" target="blank">Leprd.space</a> (my host &hearts;).</li>
</ul>


